package Clase02;

public class ConversorCelsiusToFarenheit {
    public static void main(String[] args) {
        double celsius =45;
        double faremheit;
        double conversionToFarenheit = (celsius *1.8)+32;
        faremheit = conversionToFarenheit;

        System.out.println("La temperatura de "+celsius+ "grados Celsius a Farenheit es: "+faremheit + "grados Farenheit");

    }
}
